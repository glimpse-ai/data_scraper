/*
Strip out any tags we don't want (by tag name)
 */

var acceptedBodyTags = [
  'div',
  'h1',
  'h2',
  'h3',
  'h4',
  'h5',
  'h6',
  'p',
  'ul',
  'ol',
  'li',
  'span',
  'a',
  'nav',
  'img',
  'header',
  'footer',
  'textarea',
  'button',
  'select',
  'option',
  'label',
  'form',
  'input',
  'main',
  'section',
  'table',
  'th',
  'tr',
  'td',
  'thead',
  'tbody',
  'tfoot',
  'article',
  'aside',
  'details',
  'summary',
  'data',
  'caption',
  'col',
  'colgroup',
  'dl',
  'dt',
  'dd',
  'menu',
  'menuitem',
  'figcaption',
  'figure',
  'picture',
  'optgroup',
  'fieldset',
  'legend',
  'datalist',
  'keygen',
  'output',
  'acronym',
  'abbr',
  'address',
  'b',
  'bdi',
  'bdo',
  'big',
  'blockquote',
  'center',
  'cite',
  'code',
  'del',
  'dfn',
  'em',
  'i',
  'ins',
  'kbd',
  'mark',
  'meter',
  'pre',
  'progress',
  'q',
  's',
  'samp',
  'small',
  'strike',
  'strong',
  'sub',
  'sup',
  'time',
  'tt',
  'u',
  'var',
  'wbr',
  'br',
  'hr',
  'style'
];

var selectors = ['*'];

for (var j = 0; j < acceptedBodyTags.length; j++) {
  selectors.push('not(' + acceptedBodyTags[j] + ')')
}

var unwanted = document.body.querySelectorAll(selectors.join(':'));
var el;
for (var i = unwanted.length - 1; i >= 0; i--) {
  el = unwanted[i];
  el.parentNode.removeChild(el);
}